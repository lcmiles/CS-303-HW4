public class TreeNode {
    long key;
    String type;
    String name;
    TreeNode left;
    TreeNode right;
    TreeNode p;

    public TreeNode(long key, String type, String name) {
        this.key = key;
        this. type = type;
        this.name = name;
        this.left = left;
        this.right = right;
        this.p = null;
    }
}
